package br.com.larissa.Calculadora.Model;

public class Calculator {

    public static float sum(float n1, float n2) //metodo para somar 
    {
        return n1+n2;
    }

    public static float minus(float n1, float n2)
    {
        return n1-n2;
    }

    public static float multiply(float n1, float n2)
    {
        return n1*n2;
    }

    public static float divider(float n1, float n2)
    {
        return n1/n2;
    }
}
